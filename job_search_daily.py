def load_env() -> None:
    """
    Load variables from .env file into os.environ if .env exists.
    This is a lightweight manual loader — no python-dotenv dependency needed.
    """
    if not ENV_FILE.exists():
        return
    with open(ENV_FILE) as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                key, _, value = line.partition("=")
                os.environ.setdefault(key.strip(), value.strip())


# ─── CLEANUP ──────────────────────────────────────────────────────────────────

def cleanup_old_folders(
    base_dir: Path,
    keep: int,
    trigger_after: int,
    logger: logging.Logger,
    dry_run: bool,
) -> None:
    """
    Delete dated job folders (YYYY-MM-DD) beyond the most recent `keep` days.

    Retention policy (configured via config.json cleanup section):
      - Does nothing until `trigger_after` folders have accumulated (default 7)
      - Once triggered, keeps the newest `keep` folders (default 2), deletes the rest
      - Runs at the end of every daily script execution
      - Does NOT touch logs/, seen_jobs.json, config.json, or any script files

    Example with keep=2, trigger_after=7 on day 7:
      KEEP    → 2026-03-21, 2026-03-22
      DELETE  → 2026-03-16, 2026-03-17, 2026-03-18, 2026-03-19, 2026-03-20

    Days 1-6: no deletions — folders accumulate untouched.
    """
    date_pattern = re.compile(r"^\d{4}-\d{2}-\d{2}$")
    dated_dirs = sorted(
        [d for d in base_dir.iterdir() if d.is_dir() and date_pattern.match(d.name)],
        key=lambda d: d.name,  # lexicographic sort works for YYYY-MM-DD
    )

    if len(dated_dirs) < trigger_after:
        logger.info(
            f"Cleanup: {len(dated_dirs)} dated folder(s) — "
            f"waiting until {trigger_after} before trimming"
        )
        return

    to_delete = dated_dirs[:-keep] if len(dated_dirs) > keep else []

    if not to_delete:
        logger.info(f"Cleanup: {len(dated_dirs)} dated folders present — nothing to delete (keep={keep})")
        return

    logger.info(f"Cleanup: {len(dated_dirs)} dated folders found — keeping newest {keep}, deleting {len(to_delete)}")
    for folder in to_delete:
        if dry_run:
            logger.info(f"  [DRY RUN] Would delete: {folder.name}")
        else:
            try:
                import shutil as _shutil
                _shutil.rmtree(folder)
                logger.info(f"  Deleted: {folder.name}")
            except Exception as exc:
                logger.warning(f"  Failed to delete {folder.name}: {exc}")


# ─── MAIN ─────────────────────────────────────────────────────────────────────

def main() -> int:
    """
    Entry point. Returns exit code (0 = success, 1 = partial failure, 2 = total failure).
    """
    parser = argparse.ArgumentParser(
        description="Autonomous daily job search for Bo Osinupebi",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Preview jobs found and scored — create no files"
    )
    parser.add_argument(
        "--force", action="store_true",
        help="Ignore seen_jobs.json — re-process previously seen jobs"
    )
    parser.add_argument(
        "--date", default=None, metavar="YYYY-MM-DD",
        help="Override the run date used for the output folder name"
    )
    args = parser.parse_args()

    # Determine run date
    run_date = args.date or datetime.now().strftime("%Y-%m-%d")

    # Setup
    load_env()
    logger = setup_logging(run_date)
    logger.info(f"=== Job search run: {run_date} {'[DRY RUN]' if args.dry_run else ''} ===")

    # Load config
    if not CONFIG_FILE.exists():
        logger.error(f"config.json not found at {CONFIG_FILE}")
        return 2
    with open(CONFIG_FILE) as f:
        config = json.load(f)

    # Load seen jobs (for dedup)
    seen_jobs: dict = {} if args.force else load_seen_jobs()
    if args.force:
        logger.info("--force: ignoring seen_jobs.json")
    else:
        logger.info(f"Loaded {len(seen_jobs)} previously seen job URLs")

    # ── Step 1: Fetch jobs ────────────────────────────────────────────────────
    all_jobs = fetch_all_jobs(config, logger)
    if not all_jobs:
        logger.warning("No jobs fetched — check RSS connectivity or try again later")
        return 1

    # ── Step 1b: Age filter ───────────────────────────────────────────────────
    max_age = config["search"].get("max_job_age_days", 3)
    recent_jobs = [j for j in all_jobs if is_recent(j.get("published", ""), max_age)]
    dropped = len(all_jobs) - len(recent_jobs)
    if dropped:
        logger.info(f"Age filter: dropped {dropped} jobs older than {max_age} days "
                    f"({len(recent_jobs)} remaining)")

    # ── Step 2: Phase 1 title filter — no HTTP requests ──────────────────────
    title_qualified = [j for j in recent_jobs if title_qualifies(j["title"])]
    logger.info(f"Title-qualified jobs (phase 1): {len(title_qualified)} / {len(all_jobs)}")

    # ── Step 2: Phase 2 — fetch full descriptions for title-qualified jobs ────
    # We do this in parallel to keep runtime reasonable (10 concurrent requests)
    logger.info(f"Fetching full descriptions for {len(title_qualified)} jobs...")
    with ThreadPoolExecutor(max_workers=10) as pool:
        futures = {pool.submit(fetch_full_description, j["url"]): j for j in title_qualified}
        for future in as_completed(futures):
            job = futures[future]
            full_desc = future.result()
            if full_desc:
                # Append full description to the short RSS summary
                job["description"] = (job["description"] + " " + full_desc)[:3000]

    # ── Step 2: Score and rank ────────────────────────────────────────────────
    scored: list[tuple[dict, int, float]] = []
    for job in title_qualified:
        tier, score = score_job(job, config)
        scored.append((job, tier, score))

    # Sort: Tier 1 first, then by score descending
    scored.sort(key=lambda x: (x[1], -x[2]))

    tier1 = [(j, t, s) for j, t, s in scored if t == 1]
    tier2 = [(j, t, s) for j, t, s in scored if t == 2]
    logger.info(f"Scored: {len(tier1)} Tier 1, {len(tier2)} Tier 2, "
                f"{len(scored) - len(tier1) - len(tier2)} Tier 3")

    max_results = config["search"]["max_results_per_tier"]
    candidates = tier1[:max_results]
    if len(candidates) < max_results:
        # Fill remaining slots with Tier 2 jobs
        shortfall = max_results - len(candidates)
        candidates += tier2[:shortfall]
        logger.info(f"Added {min(shortfall, len(tier2))} Tier 2 jobs to fill slots")

    if not candidates:
        logger.warning("No Tier 1 or Tier 2 jobs found today")
        return 1

    # ── Step 3: Dedup filter ──────────────────────────────────────────────────
    new_candidates = [
        (j, t, s) for j, t, s in candidates
        if j["url"] not in seen_jobs
    ]
    skipped_dedup = len(candidates) - len(new_candidates)
    if skipped_dedup:
        logger.info(f"Skipped {skipped_dedup} jobs already seen in previous runs")
    if not new_candidates:
        logger.info("All top jobs already seen — nothing new today")
        return 0

    # ── Step 4: URL validation ────────────────────────────────────────────────
    logger.info(f"Validating {len(new_candidates)} job URLs...")
    valid_candidates = []
    for job, tier, score in new_candidates:
        is_live, status_code = validate_url(job["url"])
        if is_live:
            logger.info(f"  LIVE ({status_code}): {job['title']} @ {job['company']}")
            valid_candidates.append((job, tier, score))
        else:
            logger.warning(f"  DEAD ({status_code}): {job['title']} @ {job['company']} — {job['url']}")

    if not valid_candidates:
        logger.warning("All candidate job URLs are dead — nothing to process")
        return 1

    # ── Step 5: Create run directory ──────────────────────────────────────────
    run_dir = BASE_DIR / run_date
    if not args.dry_run:
        run_dir.mkdir(exist_ok=True)
        logger.info(f"Output directory: {run_dir}")

    # ── Step 6: Process each job ──────────────────────────────────────────────
    summaries = []
    new_seen = {}

    for idx, (job, tier, score) in enumerate(valid_candidates, start=1):
        logger.info(f"\n[{idx}/{len(valid_candidates)}] {job['title']} @ {job['company']} "
                    f"(Tier {tier}, score {score:.1f})")
        summary = process_job(run_dir, idx, job, tier, score, config, logger, args.dry_run)
        summaries.append(summary)
        if not args.dry_run:
            new_seen[job["url"]] = run_date

    # ── Step 7: Save seen jobs ────────────────────────────────────────────────
    if not args.dry_run and new_seen:
        seen_jobs.update(new_seen)
        save_seen_jobs(seen_jobs)
        logger.info(f"\nSaved {len(new_seen)} new job URLs to seen_jobs.json")

    # ── Step 8: Summary ───────────────────────────────────────────────────────
    logger.info("\n=== Run Summary ===")
    success_count = 0
    for s in summaries:
        status = "✓" if s["readme"] else "✗"
        cl_status = "✓" if s["pdf_cover"] else ("md" if s["cover_letter"] else "✗")
        res_status = "✓" if s["pdf_resume"] else ("md" if s["resume"] else "✗")
        logger.info(
            f"  {status} {s['company']} — {s['title']} "
            f"| cover={cl_status} resume={res_status} | Tier {s['tier']}"
        )
        if s["readme"]:
            success_count += 1

    logger.info(f"\nProcessed {success_count}/{len(valid_candidates)} jobs successfully")
    logger.info(f"Output: {run_dir}" if not args.dry_run else "Dry run — no files written")

    # ── Step 9: Clean up old dated folders ───────────────────────────────────
    cleanup_cfg = config.get("cleanup", {})
    cleanup_old_folders(
        BASE_DIR,
        keep=cleanup_cfg.get("keep_days", 2),
        trigger_after=cleanup_cfg.get("trigger_after_days", 7),
        logger=logger,
        dry_run=args.dry_run,
    )

    logger.info("=== Done ===\n")

    if args.dry_run:
        return 0  # dry-run exits 0 whenever jobs were found and previewed
    return 0 if success_count == len(valid_candidates) else 1


if __name__ == "__main__":
    sys.exit(main())
