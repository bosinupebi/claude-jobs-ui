# ─── JOB NORMALISATION ────────────────────────────────────────────────────────
# All source-specific fetchers return a list of dicts in this common shape.
# is_remote=True gets a scoring bonus but is never a hard requirement.

def make_job(
    title: str,
    company: str,
    location: str,
    description: str,
    url: str,
    source: str,
    is_remote: bool = False,
    published: str = "",
    query: str = "",
) -> dict:
    """Normalise a job into the standard dict used throughout the pipeline."""
    return {
        "title": title.strip(),
        "company": company.strip() or "Unknown Company",
        "location": location.strip() or "Unknown",
        "description": description[:3000],
        "url": url.strip(),
        "source": source,
        "is_remote": is_remote,
        "published": published,
        "query": query,
    }


def _clean_html(raw: str) -> str:
    """Strip HTML tags and collapse whitespace."""
    text = re.sub(r"<[^>]+>", " ", raw)
    text = html.unescape(text)
    return re.sub(r"\s+", " ", text).strip()


# ─── SOURCE 1: JOB BANK CANADA RSS ───────────────────────────────────────────
# Canada's official government job board. Free Atom feed, no auth required.
# URL: https://www.jobbank.gc.ca/jobsearch/feed/jobSearchRSSfeed
# Covers all of Canada; summary HTML contains employer/location/salary fields.

def _parse_jobbank_summary(raw_html: str) -> tuple[str, str, str]:
    """Extract (company, location, salary) from Job Bank's structured HTML summary."""
    def field(label: str) -> str:
        m = re.search(
            rf"<strong>{re.escape(label)}:</strong>\s*(.*?)(?:<br|$)",
            raw_html, re.IGNORECASE | re.DOTALL,
        )
        return re.sub(r"<[^>]+>", "", m.group(1)).strip() if m else ""

    return (
        field("Employer") or "Unknown Company",
        field("Location") or "Canada",
        field("Salary") or "",
    )


def fetch_jobbank(config: dict, logger: logging.Logger) -> list[dict]:
    """
    Fetch jobs from Job Bank Canada RSS for each configured query.
    Runs queries in parallel. Not all jobs are remote — is_remote=False by default.
    """
    location = config["search"]["canada_location"]
    queries   = config["sources"]["jobbank"]["queries"]
    source    = "Job Bank Canada"
    results: list[dict] = []

    # Parse city and province from canada_location (e.g. "Toronto, ON" → "toronto", "on")
    _loc_parts = [p.strip().lower() for p in location.split(",")]
    _allowed_city     = _loc_parts[0] if _loc_parts else ""
    _allowed_province = _loc_parts[1] if len(_loc_parts) > 1 else ""

    def _fetch_one(query: str) -> list[dict]:
        url = (
            f"https://www.jobbank.gc.ca/jobsearch/feed/jobSearchRSSfeed"
            f"?searchstring={quote_plus(query)}"
            f"&locationstring={quote_plus(location)}"
        )
        try:
            feed = feedparser.parse(url)
            jobs = []
            skipped = 0
            for e in feed.entries:
                raw = getattr(e, "summary", "") or ""
                company, loc, salary = _parse_jobbank_summary(raw)
                desc = _clean_html(raw)
                if salary:
                    desc = f"Salary: {salary}. {desc}"
                # Job Bank's locationstring is not a strict filter — it returns all of Canada.
                # Enforce canada_location from config: keep only matching city/province, national, or remote.
                loc_lower = loc.lower()
                is_remote_loc = "remote" in loc_lower
                is_allowed_location = (
                    (_allowed_city     and _allowed_city     in loc_lower) or
                    (_allowed_province and _allowed_province in loc_lower)
                )
                is_national = loc_lower in ("canada", "") or loc_lower.startswith("canada,")
                if not (is_allowed_location or is_national or is_remote_loc):
                    skipped += 1
                    continue
                if link := getattr(e, "link", ""):
                    jobs.append(make_job(
                        title=getattr(e, "title", "Unknown").strip(),
                        company=company, location=loc, description=desc,
                        url=link, source=source, is_remote=is_remote_loc,
                        published=getattr(e, "published", ""), query=query,
                    ))
            if skipped:
                logger.info(f"  Job Bank '{query}': dropped {skipped} jobs outside '{location}'")
            logger.info(f"  Job Bank '{query}': {len(jobs)} entries")
            return jobs
        except Exception as exc:
            logger.warning(f"  Job Bank '{query}' failed: {exc}")
            return []

    with ThreadPoolExecutor(max_workers=len(queries)) as pool:
        for batch in pool.map(_fetch_one, queries):
            results.extend(batch)
    return results


